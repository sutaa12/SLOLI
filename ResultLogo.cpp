//=============================================================================
// 初期化関数
//=============================================================================
void RESULTLOGOIMAGE::InitResultLogo(void)//ポリゴン初期化で結果を返す
{
	this->c_LogoBase.InitBase(RESLUT_X,RESLUT_Y,0,0,RESULT_W,RESULT_H,0);
	this->c_LogoPolygon.InitPolygon(RLOGO1,RESULT_W,RESULT_H,RESLUT_X,RESLUT_Y,1);

}
//=============================================================================
// 終了処理関数
//=============================================================================
void RESULTLOGOIMAGE::UnInitResultLogo(void)//ポリゴン終了処理
{
	this->c_LogoPolygon.UninitPolygon();
}
//=============================================================================
// アップデート関数
//=============================================================================
void RESULTLOGOIMAGE::UpdateResultLogo(void)//ポリゴンアップデート
{
	this->c_LogoPolygon.UpdatePolygon(this->c_LogoBase.chkWitdth(),this->c_LogoBase.chkHeight(),this->c_LogoBase.chkPosX(),this->c_LogoBase.chkPosX(),0);
}
//=============================================================================
// 描画関数
//=============================================================================
void RESULTLOGOIMAGE::DrawResultLogo(void)//ポリゴン描画
{
	this->c_LogoPolygon.DrawPolygon(0);
}