//=============================================================================
// 初期化関数
//=============================================================================
void TITLEBGIMAGE::InitTitleBG(void)//ポリゴン初期化で結果を返す
{

	this->BGPolygon.InitPolygon(TBG1,TITLEBGWH,TITLEBGWH,T_X,-50,1);
	this->BGBase.InitBase(T_X,-50,0,0,TITLEBGWH,TITLEBGWH,0);

}
//=============================================================================
// 終了処理関数
//=============================================================================
void TITLEBGIMAGE::UnInitTitleBG(void)//ポリゴン終了処理
{

	this->BGPolygon.UninitPolygon();

}
//=============================================================================
// アップデート関数
//=============================================================================
void TITLEBGIMAGE::UpdateTitleBG(void)//ポリゴンアップデート
{
	this->BGPolygon.UpdatePolygon(this->BGBase.chkWitdth(),this->BGBase.chkHeight(),this->BGBase.chkPosX(),this->BGBase.chkPosY(),0);
}
//=============================================================================
// 描画関数
//=============================================================================
void TITLEBGIMAGE::DrawTitleBG(void)//ポリゴン描画
{
	//ポリゴン表示
	this->BGPolygon.DrawPolygon(0);
}